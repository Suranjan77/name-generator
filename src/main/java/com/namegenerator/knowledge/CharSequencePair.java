package com.namegenerator.knowledge;

public final class CharSequencePair {

    private char y;
    private char x;

    public CharSequencePair(char x, char y) {
        this.x = x;
        this.y = y;
    }

    public char getY() {
        return y;
    }

    public void setY(char y) {
        this.y = y;
    }

    public char getX() {
        return x;
    }

    public void setX(char x) {
        this.x = x;
    }
}
